class Product {
  final String code;
  final String name;
  final String description;
  final double rating;
  final int stock;
  final int price;
  final String image;

  Product({
    required this.code,
    required this.name,
    required this.description,
    required this.price,
    required this.image,
    required this.stock,
    required this.rating,
  });
}
